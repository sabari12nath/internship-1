# Simple program to demonstrate basic Python concepts

# Print a greeting
name = print(input("Enter your name: "))
print("Hello, {name}!")

# Basic math operations
number = int(input("Enter a number: "))
print("Double of {number} is: {number * 2}")
print("Square of {number} is: {number ** 2}")

# Simple if-else statement
if number > 10:
    print("That's a big number!")
else:
    print("That's a small number!")